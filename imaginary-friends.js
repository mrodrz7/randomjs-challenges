// Specifically 33% of their friends are imaginary....

// Create a function that takes in the total amount of friends
// and returns the total imaginary friends
// Round your answer to the nearest whole number


const numImaginaryFriends = (total) => {
    const imaginary = total * .33
    return Math.round(imaginary)
  }
  
  
  
  
  
